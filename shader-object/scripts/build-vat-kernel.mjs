import { spawnSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const source = path.join(root, 'assembly/vat-pack-kernel.ts');
const output = path.join(root, 'src/extensions/VATBuilder/vat-pack-kernel.generated.ts');
const compiler = path.join(root, 'node_modules/.bin/asc');
const variants = [
  { name: 'SCALAR', flags: ['--disable', 'simd', '--disable', 'relaxed-simd'] },
  { name: 'SIMD', flags: ['--enable', 'simd'] },
  { name: 'RELAXED_SIMD', flags: ['--enable', 'simd', '--enable', 'relaxed-simd'] },
];
const modules = [];
for (const variant of variants) {
  const wasm = path.join(root, `build/vat-pack-kernel-${variant.name.toLowerCase()}.wasm`);
  const result = spawnSync(compiler, [
    source, '--outFile', wasm, '--optimize', '--runtime', 'stub', ...variant.flags,
  ], { stdio: 'inherit' });
  if (result.status !== 0) process.exit(result.status ?? 1);
  modules.push(`export const VAT_PACK_KERNEL_${variant.name}_BASE64 = '${readFileSync(wasm).toString('base64')}';`);
}
writeFileSync(output, `// Generated by scripts/build-vat-kernel.mjs.\n${modules.join('\n')}\n`);
