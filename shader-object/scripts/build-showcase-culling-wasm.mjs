import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

import { NullEngine } from '@babylonjs/core/Engines/nullEngine.js';
import { EqShowcaseActor, EqShowcaseContainer } from '../dist/index.js';

const engine = new NullEngine({ renderWidth: 1, renderHeight: 1, textureSize: 1 });
try {
  await EqShowcaseContainer.initialize(engine, {
    backend: 'datatex',
    wasm: true,
    extra: EqShowcaseActor,
    logShaderCode: false,
    logAscCode: false,
  });
  const bytes = EqShowcaseContainer.compiledWasmBytes;
  if (!bytes?.length || !WebAssembly.validate(bytes)) {
    throw new Error('Showcase culling compiler did not produce a valid WASM module');
  }
  const actorStride = EqShowcaseActor.getSchema().headerFloatCount * 4;
  const output = `// Generated by scripts/build-showcase-culling-wasm.mjs.\n` +
    `// EqShowcaseActor AoS stride: ${actorStride} bytes.\n` +
    `export const SHOWCASE_CULLING_WASM_BASE64 = '${Buffer.from(bytes).toString('base64')}';\n`;
  const target = fileURLToPath(new URL('../src/showcase/showcase-culling-wasm.generated.ts', import.meta.url));
  await writeFile(target, output);
  console.log(`Wrote ${bytes.length} bytes (${actorStride}-byte actor stride)`);
} finally {
  engine.dispose();
}
