import { readFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptsDirectory = fileURLToPath(new URL(".", import.meta.url));
const clientRoot = resolve(scriptsDirectory, "..");
const repositoryRoot = resolve(clientRoot, "..");
const serverPath = resolve(
  repositoryRoot,
  "serverjs/src/protocol/generated/net-structs.ts",
);
const clientPath = resolve(
  clientRoot,
  "src/Game/Net/generated/net-structs.ts",
);

const [serverSource, clientSource, workspacePackageSource] = await Promise.all([
  readFile(serverPath, "utf8"),
  readFile(clientPath, "utf8"),
  readFile(resolve(repositoryRoot, "shader-object/package.json"), "utf8"),
]);

if (serverSource !== clientSource) {
  throw new Error(
    "Client and ServerJS Shado net bindings differ; run npm run net:generate",
  );
}
if (!serverSource.startsWith("// Generated by Shado")) {
  throw new Error("Net bindings are not marked as Shado-generated output");
}

const workspaceVersion = JSON.parse(workspacePackageSource).version;
console.log(
  `Client and ServerJS Shado net bindings are byte-identical (@knervous/shado ${workspaceVersion})`,
);
